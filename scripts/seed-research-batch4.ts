async function seed() {
  console.log("UXMind Research Seeder — Batch 4\n");
  let inserted = 0, skipped = 0, rejected = 0;

  for (const entry of entries) {
    const result = calculateScore(entry.research_type, entry.scores);
    const slug = slugify(entry.title);
    console.log(`  [${result.passed ? "PASS" : "FAIL"}] ${result.breakdown.weighted_total.toFixed(1)}/100 — ${entry.title.slice(0, 65)}`);
    if (!result.passed) { rejected++; continue; }

    const { data: existing } = await supabase.from("research_entries").select("id").eq("slug", slug).limit(1);
    if (existing && existing.length > 0) { console.log(`    [skip] Already exists`); skipped++; continue; }

    const { error } = await supabase.from("research_entries").insert({
      title: entry.title, slug, attributed_summary: entry.attributed_summary,
      source_url: entry.source_url, source_name: entry.source_name,
      authors: entry.authors, publication_date: entry.publication_date,
      research_type: entry.research_type, site_contexts: entry.site_contexts,
      quality_score: result.breakdown.weighted_total, scoring_track: entry.research_type,
      scoring_breakdown: result.breakdown, key_findings: entry.key_findings,
      methodology_summary: entry.methodology_summary, sample_size: entry.sample_size,
      limitations: entry.limitations, tags: entry.tags, status: "published",
    });
    if (error) { console.error(`    [error] ${error.message}`); } else { inserted++; console.log(`    [inserted]`); }
  }
  console.log(`\nComplete: ${inserted} inserted, ${skipped} skipped, ${rejected} rejected`);
}
seed().catch((err) => { console.error("Fatal:", err); process.exit(1); });
